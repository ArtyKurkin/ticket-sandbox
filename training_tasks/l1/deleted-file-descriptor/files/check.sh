#!/usr/bin/env bash

echo "Проверка задания: удалённый файл держится открытым дескриптором"
echo

ok=true

# Ищем любой удалённый файл, который всё ещё держится открытым процессом.
# lsof помечает такие записи как (deleted).
deleted_held="$(lsof 2>/dev/null | grep -E '/var/log/app/huge\.log.*\(deleted\)' | wc -l)"

if [ "$deleted_held" -gt 0 ]; then
  echo "❌ Найден процесс, который всё ещё держит удалённый файл huge.log (deleted)."
  echo "   Место не освобождено. Нужно завершить/перезапустить процесс,"
  echo "   который держит дескриптор."
  ok=false
else
  echo "✅ Нет процессов, удерживающих удалённый huge.log — место освобождено."
fi

echo
if [ "$ok" = true ]; then
  echo "Задание пройдено."
  exit 0
fi
echo "Задание ещё не выполнено. Посмотри сообщения выше."
exit 1
